#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*buf;

	buf = (unsigned char*)s;
	i = 0;
	while (i < n)
	{
		buf[i] = 0;
		i = i + 1;
	}
}
