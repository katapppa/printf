#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		n = n * -1;
		ft_putchar('-');
		if (n == -2147483648)
		{
			ft_putnbr(214748364);
			ft_putnbr(8);
		}
	}
	if (0 <= n && n <= 9)
		ft_putchar(n + '0');
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putchar(n % 10 + '0');
	}
}
